#Program To Perform Mathematical operations#
var1 = input('Enter the first number:')
var2 = input('Enter the second number:')
addition = int(var1) + int(var2)
print("Addition",addition)
subtraction = int(var1) - int(var2)
print("Subtraction", subtraction)
multiplication = int(var1) * int(var2)
print("Multiplication", multiplication)
division = int(var1) / int(var2)
print("Division", division)


